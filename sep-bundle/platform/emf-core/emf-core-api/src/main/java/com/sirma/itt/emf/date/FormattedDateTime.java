package com.sirma.itt.emf.date;

/**
 * Used for date conversion.
 * 
 * @author BBonev
 */
public class FormattedDateTime {

	/** The formatted. */
	private final String formatted;

	/**
	 * Instantiates a new formatted date.
	 * 
	 * @param formatted
	 *            the formatted
	 */
	public FormattedDateTime(String formatted) {
		this.formatted = formatted;
	}

	/**
	 * Getter method for formatted.
	 * 
	 * @return the formatted
	 */
	public String getFormatted() {
		return formatted;
	}

}
